package common

// []map倒序函数
func Reverse(arr *[]map[string]interface{}) {
	var temp map[string]interface{}
	length := len(*arr)
	for i := 0; i < length/2; i++ {
		temp = (*arr)[i]
		(*arr)[i] = (*arr)[length-1-i]
		(*arr)[length-1-i] = temp
	}
}
